                    clsBilletage.BI_QUANTITESORTIE = int.Parse(clsBilletageDTO.BI_QUANTITESORTIE.ToString());
                    clsBilletage.TYPEOPERATION = clsBilletageDTO.TYPEOPERATION.ToString();

                    clsObjetEnvoi.OE_A = clsBilletageDTO.clsObjetEnvoi.OE_A;
                    clsObjetEnvoi.OE_Y = clsBilletageDTO.clsObjetEnvoi.OE_Y;

                    clsBilletages.Add(clsBilletage);
                }
                clsObjetRetour.SetValue(true, clsBilletageWSBLL.pvgAjouterListe(clsDonnee, clsBilletages, clsObjetEnvoi));
                if (clsObjetRetour.OR_BOOLEEN)
                {
                    DataSet = new DataSet();
                    DataRow dr = dt.NewRow();
                    dr["SL_CODEMESSAGE"] = "00";
                    dr["SL_RESULTAT"] = "TRUE";
                    dr["SL_MESSAGE"] = "L'opération s'est réalisée avec succès";
                    dt.Rows.Add(dr);
                    DataSet.Tables.Add(dt);
                    json = JsonConvert.SerializeObject(DataSet, Formatting.Indented);
                }
            }
             catch (SqlException SQLEx)
            {
                DataSet = new DataSet();
                DataRow dr = dt.NewRow();
                dr["SL_CODEMESSAGE"] = "99";
                dr["SL_RESULTAT"] = "FALSE";
                dr["SL_MESSAGE"] = (SQLEx.Number == 2601 || SQLEx.Number == 2627) ? clsMessagesWSBLL.pvgTableLibelle(clsDonnee, "GNE0003").MS_LIBELLEMESSAGE : SQLEx.Message;
