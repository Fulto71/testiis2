                    ZenithWebServeur.BOJ.clsBilletage clsBilletage = new ZenithWebServeur.BOJ.clsBilletage();

                    clsBilletage.AG_CODEAGENCE = clsBilletageDTO.AG_CODEAGENCE.ToString();
                    clsBilletage.MC_DATEPIECE = DateTime.Parse(clsBilletageDTO.MC_DATEPIECE.ToString());
                    clsBilletage.BI_NUMPIECE = clsBilletageDTO.BI_NUMPIECE.ToString();
                    clsBilletage.BI_NUMSEQUENCE = clsBilletageDTO.BI_NUMSEQUENCE.ToString();
                    clsBilletage.CB_CODECOUPURE = clsBilletageDTO.CB_CODECOUPURE.ToString();
                    clsBilletage.PL_CODENUMCOMPTE = clsBilletageDTO.PL_CODENUMCOMPTE.ToString();
